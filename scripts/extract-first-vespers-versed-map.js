function chapterOf(bare) {
  const m = bare.match(/^Psalm (\d+)$/)
  if (!m) throw new Error(`bare ref must be "Psalm N", got "${bare}"`)
  return parseInt(m[1], 10)
}

function loadPdfLines() {
  return fs.readFileSync(PDF_PATH, 'utf8').split('\n')
}

function verifyAnchorAndBody(lines, entry) {
  const errors = []
  const actualAnchor = lines[entry.anchor_line - 1] // 1-based → 0-based
  if (actualAnchor !== entry.anchor_text) {
    errors.push(
      `anchor mismatch at L${entry.anchor_line}: expected "${entry.anchor_text}", got "${actualAnchor}"`,
    )
  }
  const [bodyStart] = entry.body_lines.split('-').map((s) => parseInt(s, 10))
  const actualBodyFirst = lines[bodyStart - 1]
  if (actualBodyFirst !== entry.body_first_line_fingerprint) {
    errors.push(
      `body fingerprint mismatch at L${bodyStart}: expected "${entry.body_first_line_fingerprint}", got "${actualBodyFirst}"`,
    )
  }
  return errors
}

function buildVersedMap() {
  const lines = loadPdfLines()
  const allErrors = []
  for (const entry of CANONICAL) {
    const errs = verifyAnchorAndBody(lines, entry)
    if (errs.length > 0) {
      allErrors.push({ bare: entry.bare, errors: errs })
    }
  }
  if (allErrors.length > 0) {
    return { ok: false, errors: allErrors }
  }
  // Sort by chapter number for stable output (16, 113, 122, 130, 142, 147).
  const sorted = [...CANONICAL].sort((a, b) => chapterOf(a.bare) - chapterOf(b.bare))
  const out = {}
  for (const e of sorted) {
    out[e.bare] = {
      versed: e.versed,
      evidence_pdf_lines: e.body_lines,
      evidence_pdf_page: e.pdf_page,
      case_verdict: e.case_verdict,
      case_notes: e.case_notes,
      trigger_wi_c: e.trigger_wi_c,
    }
  }
  return { ok: true, map: out }
}

function serialize(map) {
  return JSON.stringify(map, null, 2) + '\n'
}

function main() {
  const checkOnly = process.argv.includes('--check')
  if (!fs.existsSync(PDF_PATH)) {
    console.error(`ERROR: ${PDF_PATH} not found. Run scripts/reextract-pdf-pages.sh first.`)
    process.exit(1)
  }
  const result = buildVersedMap()
  if (!result.ok) {
    console.error('ERROR: PDF anchor/fingerprint verification failed.')
    for (const e of result.errors) {
      console.error(`  ${e.bare}:`)
      for (const msg of e.errors) console.error(`    ${msg}`)
    }
    console.error('JSON output WAS NOT written. Update CANONICAL[].anchor_line/body_lines after re-extracting PDF.')
    process.exit(1)
  }
  const serialized = serialize(result.map)
  if (checkOnly) {
    if (!fs.existsSync(OUT_PATH)) {
      console.error(`ERROR: ${OUT_PATH} missing. Run without --check to generate.`)
      process.exit(1)
    }
    const existing = fs.readFileSync(OUT_PATH, 'utf8')
    if (existing !== serialized) {
      console.error('ERROR: existing JSON differs from script output. Re-run without --check to refresh.')
      console.error('--- diff hint ---')
      const a = existing.split('\n')
      const b = serialized.split('\n')
      const N = Math.min(a.length, b.length)
      let shown = 0
      for (let i = 0; i < N && shown < 8; i++) {
        if (a[i] !== b[i]) {
          console.error(`  L${i + 1}\n    existing: ${a[i]}\n    script:   ${b[i]}`)
          shown++
        }
      }
      process.exit(1)
    }
    console.log(`OK — ${OUT_PATH} byte-equal with script output (${Object.keys(result.map).length} entries).`)
    process.exit(0)
  }
  fs.writeFileSync(OUT_PATH, serialized, 'utf8')
  console.log(
    `Wrote ${OUT_PATH} (${Object.keys(result.map).length} entries: ${Object.keys(result.map).join(', ')})`,
  )
  const wic = Object.entries(result.map).filter(([, v]) => v.trigger_wi_c).map(([k]) => k)
  if (wic.length > 0) console.log(`WI-C trigger: ${wic.join(', ')}`)
}

if (require.main === module) main()

