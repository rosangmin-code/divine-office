#!/usr/bin/env node
// Scan test files for `@fr FR-XXX` comments and FR/NFR IDs embedded in test
// titles, then produce `docs/traceability-auto.md`. With `--check`, verify
// every tagged ID is also present in the hand-maintained
// `docs/traceability-matrix.md` — used in CI to catch matrix drift.

import { readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs'
import { join, resolve } from 'node:path'

const ROOT = resolve(process.cwd())
const OUT = join(ROOT, 'docs/traceability-auto.md')
const MANUAL_MATRIX = join(ROOT, 'docs/traceability-matrix.md')

const INCLUDE_DIRS = ['e2e', 'src', 'scripts']
const FILE_SUFFIX = /\.(spec|test)\.ts$/
const ID_RE = /\b(?:FR|NFR)-\d+[a-z]?\b/g
const TAG_RE = /\/\/\s*@fr\s+([^\n]+)/

function walk(dir) {
  const out = []
  let entries
  try {
    entries = readdirSync(dir)
  } catch {
    return out
  }
  for (const name of entries) {
    if (name === 'node_modules' || name.startsWith('.')) continue
    const full = join(dir, name)
    const st = statSync(full)
    if (st.isDirectory()) {
      out.push(...walk(full))
    } else if (FILE_SUFFIX.test(name)) {
      out.push(full)
    }
  }
  return out
}

function collect(filePath) {
  const src = readFileSync(filePath, 'utf8')
  const lines = src.split('\n')
  const hits = new Map() // id -> Set<title>

  const add = (id, title) => {
    if (!hits.has(id)) hits.set(id, new Set())
    hits.get(id).add(title)
  }

  lines.forEach((line, i) => {
    const tag = TAG_RE.exec(line)
    if (tag) {
      const ids = tag[1].match(ID_RE) ?? []
      // Look ahead a few lines for the test/describe/it title that the
      // comment is annotating.
      const next = lines
        .slice(i + 1, i + 10)
        .find((l) => /\b(test|describe|it)\(/.test(l))
      const title =
        next?.match(/['"`](.+?)['"`]/)?.[1] ?? '(tagged, title unknown)'
      for (const id of ids) add(id, title)
    }

    const tm = /\b(test|it)\(\s*['"`](.+?)['"`]/.exec(line)
    if (tm) {
      for (const id of tm[2].match(ID_RE) ?? []) add(id, tm[2])
    }
  })

  return hits
}

// Gather
const files = []
for (const d of INCLUDE_DIRS) {
  files.push(...walk(join(ROOT, d)))
}

const fileToIds = new Map()
const idToFiles = new Map()

for (const file of files) {
  const rel = file.slice(ROOT.length + 1)
  const hits = collect(file)
  if (hits.size === 0) continue
  fileToIds.set(rel, hits)
  for (const id of hits.keys()) {
    if (!idToFiles.has(id)) idToFiles.set(id, new Set())
    idToFiles.get(id).add(rel)
  }
}

// Sort: FR before NFR, then numeric, then alphabetic suffix
const idSort = (a, b) => {
  const [pa, sa] = a.split('-')
  const [pb, sb] = b.split('-')
  if (pa !== pb) return pa === 'FR' ? -1 : 1
  const na = parseInt(sa, 10)
  const nb = parseInt(sb, 10)
  if (na !== nb) return na - nb
  return sa.localeCompare(sb)
}

const ids = [...idToFiles.keys()].sort(idSort)

// --check mode: diff against hand-maintained matrix
if (process.argv.includes('--check')) {
  let manual
  try {
    manual = readFileSync(MANUAL_MATRIX, 'utf8')
  } catch (err) {
    console.error(`Cannot read ${MANUAL_MATRIX}: ${err.message}`)
    process.exit(2)
  }
  const missing = ids.filter((id) => !manual.includes(id))
  if (missing.length > 0) {
    console.error(
      `❌ Matrix drift: ${missing.length} tagged ID(s) missing from docs/traceability-matrix.md:`,
    )
    for (const id of missing) {
      const fileList = [...idToFiles.get(id)].join(', ')
      console.error(`   ${id}  (in ${fileList})`)
    }
    console.error(
      '\nFix by adding rows to docs/traceability-matrix.md or removing the stale @fr tag.',
    )
    process.exit(1)
  }
  console.log(
    `✅ OK — ${ids.length} tagged ID(s) all present in docs/traceability-matrix.md`,
  )
  process.exit(0)
}

// Generate markdown
let md = '# Auto-generated FR/NFR → Test map\n\n'
md += '> Generated by `scripts/generate-test-fr-map.mjs` from `@fr` comments\n'
md += '> and FR/NFR IDs in test titles. **Do not edit by hand.**\n'
md += '> The curated matrix lives in [`docs/traceability-matrix.md`](./traceability-matrix.md).\n\n'
md += `Scan: ${fileToIds.size} test files contributed ${ids.length} unique IDs.\n\n`
md += '| ID | Test file | Test title(s) |\n|---|---|---|\n'

for (const id of ids) {
  const files = [...idToFiles.get(id)].sort()
  for (const file of files) {
    const titles = [...fileToIds.get(file).get(id)]
      .map((t) => t.replace(/\|/g, '\\|'))
      .join('<br>')
    md += `| ${id} | \`${file}\` | ${titles} |\n`
  }
}

writeFileSync(OUT, md)
console.log(
  `Wrote ${fileToIds.size} test files × ${ids.length} IDs → ${OUT}`,
)
